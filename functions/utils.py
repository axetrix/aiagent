from typing_extensions import List, Dict, Callable

from google.genai import types

from functions.get_files_info import get_files_info, schema_get_files_info
from functions.get_file_content import get_file_content, schema_get_file_content
from functions.write_file import write_file, schema_write_file
from functions.run_python_file import run_python_file, schema_run_python_file

WORKING_DIRECTORY = "./calculator"

ALLOWED_FUNCTIONS: Dict[str, Callable[..., str]] = {
    "get_files_info": get_files_info,
    "get_file_content": get_file_content,
    "write_file": write_file,
    "run_python_file": run_python_file,
}

available_functions = types.Tool(
    function_declarations=[
        schema_get_files_info,
        schema_get_file_content,
        schema_run_python_file,
        schema_write_file,
    ]
)


def call_function(
    function_call_part: types.FunctionCall, verbose: bool = False
) -> types.Content:
    if verbose:
        print(
            f"Calling function: {function_call_part.name}({function_call_part.args})"
        )
    else:
        print(f" - Calling function: {function_call_part.name}")

    function_name = function_call_part.name
    func_args = function_call_part.args or {}

    if not function_name:
        raise ValueError("Function not specified")

    if function_name not in ALLOWED_FUNCTIONS:
        return types.Content(
            role="tool",
            parts=[
                types.Part.from_function_response(
                    name=function_name,
                    response={"error": f"Unknown function: {function_name}"},
                )
            ],
        )

    func_args["working_directory"] = WORKING_DIRECTORY

    function_result = ALLOWED_FUNCTIONS[function_name](**func_args)

    return types.Content(
        role="tool",
        parts=[
            types.Part.from_function_response(
                name=function_name,
                response={"result": function_result},
            )
        ],
    )
